package org.club.loaddb.vo;

/**
 *
 * @author Osiel
 */
public class RamaVO {
    
    private int intIdContaminante = -1;
    
    private String stringFecha = "";
    
    private int intHora = -1;
    
    private String stringContaminante = "";
    
    private String stringEstacion = "";
    
    private String stringConcentracion = "";
    
    private String stringUnidad = "";
            
    public int getIntIdContaminante() {
        return intIdContaminante;
    }
    
    public void setIntIdContaminante(int intIdContaminante) {
        this.intIdContaminante = intIdContaminante;
    }
    
    public String getStringFecha() {
        return stringFecha;
    }
    
    public void setStringFecha(String stringFecha) {
        this.stringFecha = stringFecha;
    }
    
    public int getIntHora() {
        return intHora;
    }
    
    public void setIntHora(int intHora) {
        this.intHora = intHora;
    }
    
    public String getStringContaminante() {
        return stringContaminante;
    }
    
    public void setStringContaminante(String stringContaminante) {
        this.stringContaminante = stringContaminante;
    }
    
    public String getStringEstacion() {
        return stringEstacion;
    }
    
    public void setStringEstacion(String stringEstacion) {
        this.stringEstacion = stringEstacion;
    }
    
    public String getStringConcentracion() {
        return stringConcentracion;
    }
    
    public void setStringConcentracion(String stringConcentracion) {
        this.stringConcentracion = stringConcentracion;
    }
    
    public String getStringUnidad() {
        return stringUnidad;
    }
    
    public void setStringUnidad(String stringUnidad) {
        this.stringUnidad = stringUnidad;
    }
}
